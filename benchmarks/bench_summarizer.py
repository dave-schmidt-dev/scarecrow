@dataclass
class RunSpec:
    """Describes a single benchmark run."""

    label: str
    backend: str
    model_key: str | None  # key into _MODEL_PATTERNS (GGUF) or None
    kv_bits: float | None = None  # MLX TurboQuant only


def _build_run_specs(
    backends: list[str],
    kv_bits_list: list[float],
) -> list[RunSpec]:
    """Build the list of RunSpec entries, skipping unavailable combinations."""
    specs: list[RunSpec] = []

    for backend in backends:
        if backend == "gguf":
            # gemma4 GGUF — only if available
            gguf_gemma4 = _discover_gguf(model_pattern=_MODEL_PATTERNS["gemma4"])
            if gguf_gemma4 is not None:
                specs.append(
                    RunSpec(
                        label="gguf/gemma-4",
                        backend="gguf",
                        model_key="gemma4",
                    )
                )
            else:
                print(
                    "  [skip] gguf/gemma-4: no GGUF found for pattern "
                    f"{_MODEL_PATTERNS['gemma4']!r}"
                )

        elif backend == "mlx":
            # Check that mlx-vlm is installed
            try:
                import mlx_vlm  # noqa: F401

                mlx_available = True
            except ImportError:
                mlx_available = False

            if not mlx_available:
                print(
                    "  [skip] mlx: mlx-vlm not installed. "
                    "Run: uv sync --extra mlx-summarizer"
                )
                continue

            # Default MLX baseline (always included)
            mlx_model_id = _config_module.config.SUMMARIZER_MLX_MODEL_ID
            specs.append(
                RunSpec(
                    label=f"mlx/{mlx_model_id.split('/')[-1]}",
                    backend="mlx",
                    model_key=None,
                )
            )

            # MLX + TurboQuant KV cache — one RunSpec per kv_bits value
            for kv_bits in kv_bits_list:
                specs.append(
                    RunSpec(
                        label=f"mlx/{mlx_model_id.split('/')[-1]}/kv{kv_bits:g}",
                        backend="mlx",
                        model_key=None,
                        kv_bits=kv_bits,
                    )
                )

        else:
            print(f"  [skip] unknown backend: {backend!r}")

    return specs


