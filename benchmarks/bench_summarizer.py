def _write_report(
    all_results: dict[str, list[RunResult]],
    output_dir: Path,
    model: str,
) -> Path:
    """Write a Markdown KV-cache evaluation report to output_dir."""
    date_str = datetime.date.today().isoformat()
    lines: list[str] = [
        "# TurboQuant KV-cache Evaluation",
        "",
        f"Date: {date_str}",
        f"Model: {model}",
        "Temperature: 0.3",
        "",
    ]

    # Collect per-config aggregate data for the summary table
    # {label: {wall_s: [], tok_s: [], gpu_gb: [], rouge_l: [], preservation: []}}
    agg: dict[str, dict[str, list[float]]] = {}

    for transcript_name, results in all_results.items():
        # Count lines in the transcript (approximate via summary footer)
        # We don't have easy access to the jsonl here, so use transcript_words as proxy
        baseline_result = next(
            (r for r in results if r.spec.kv_bits is None and r.ok), None
        )
        baseline_text = (
            baseline_result.summary_path.read_text(encoding="utf-8")
            if baseline_result and baseline_result.summary_path
            else ""
        )

        header_line = f"## {transcript_name}"
        lines.append(header_line)
        lines.append("")
        lines.append(
            "| Config | Wall (s) | Tok/s | Words | GPU Peak (GB)"
            " | GPU Delta | ROUGE-L | Action Items |"
        )
        lines.append(
            "|--------|----------|-------|-------|---------------"
            "|-----------|---------|--------------|"
        )

        for r in results:
            config_label = r.spec.label
            if r.error:
                lines.append(f"| {config_label} | ERROR | — | — | — | — | — | — |")
                continue

            wall_s = f"{r.wall_s:.1f}"
            tok_s = f"{r.tok_per_s:.1f}"
            words = str(r.summary_words)
            gpu_peak = f"{r.peak_memory_gb:.2f}" if r.peak_memory_gb else "—"

            is_baseline = r.spec.kv_bits is None and r.spec.backend == "mlx"

            # GPU delta vs baseline
            if is_baseline or baseline_result is None:
                gpu_delta = "—"
            else:
                delta = r.peak_memory_gb - baseline_result.peak_memory_gb
                gpu_delta = f"{delta:+.2f} GB"

            # Quality metrics vs baseline
            if is_baseline or not baseline_text or not r.summary_path:
                rouge_str = "—"
                action_str = "—"
                rl_val = None
                pres_val = None
            else:
                variant_text = r.summary_path.read_text(encoding="utf-8")
                quality = _compare_quality(baseline_text, variant_text)
                rl_val = quality["rouge_l"]
                pres_val = quality["preservation_rate"]
                rouge_str = f"{rl_val:.3f}"
                ai_base = quality["action_items_baseline"]
                ai_pres = quality["action_items_preserved"]
                action_str = f"{ai_pres}/{ai_base}"

            lines.append(
                f"| {config_label} | {wall_s} | {tok_s} | {words} | "
                f"{gpu_peak} | {gpu_delta} | {rouge_str} | {action_str} |"
            )

            # Accumulate for aggregate
            if config_label not in agg:
                agg[config_label] = {
                    "wall_s": [],
                    "tok_s": [],
                    "gpu_gb": [],
                    "rouge_l": [],
                    "preservation": [],
                }
            agg[config_label]["wall_s"].append(r.wall_s)
            agg[config_label]["tok_s"].append(r.tok_per_s)
            if r.peak_memory_gb:
                agg[config_label]["gpu_gb"].append(r.peak_memory_gb)
            if rl_val is not None:
                agg[config_label]["rouge_l"].append(rl_val)
            if pres_val is not None:
                agg[config_label]["preservation"].append(pres_val)

        lines.append("")

    # Aggregate table (only meaningful when multiple transcripts)
    if len(all_results) > 1:
        lines.append("## Aggregate")
        lines.append("")
        lines.append(
            "| Config | Avg Wall (s) | Avg Tok/s | Avg GPU Peak (GB)"
            " | Avg GPU Delta | Avg ROUGE-L | Min Preservation |"
        )
        lines.append(
            "|--------|-------------|-----------|-------------------"
            "|---------------|-------------|-----------------|"
        )

        # Find baseline label for delta computation
        baseline_label = next(
            (lbl for lbl in agg if "mlx" in lbl and "/kv" not in lbl),
            None,
        )
        baseline_avg_gpu = (
            sum(agg[baseline_label]["gpu_gb"]) / len(agg[baseline_label]["gpu_gb"])
            if baseline_label and agg[baseline_label]["gpu_gb"]
            else None
        )

        for lbl, data in agg.items():
            avg_wall = (
                sum(data["wall_s"]) / len(data["wall_s"]) if data["wall_s"] else 0
            )
            avg_tok = sum(data["tok_s"]) / len(data["tok_s"]) if data["tok_s"] else 0
            avg_gpu = (
                sum(data["gpu_gb"]) / len(data["gpu_gb"]) if data["gpu_gb"] else None
            )
            avg_rl = (
                sum(data["rouge_l"]) / len(data["rouge_l"]) if data["rouge_l"] else None
            )
            min_pres = min(data["preservation"]) if data["preservation"] else None

            gpu_str = f"{avg_gpu:.2f}" if avg_gpu is not None else "—"
            if (
                avg_gpu is not None
                and baseline_avg_gpu is not None
                and lbl != baseline_label
            ):
                delta = avg_gpu - baseline_avg_gpu
                gpu_delta_str = f"{delta:+.2f} GB"
            else:
                gpu_delta_str = "—"
            rl_str = f"{avg_rl:.3f}" if avg_rl is not None else "—"
            pres_str = f"{min_pres:.3f}" if min_pres is not None else "—"

            lines.append(
                f"| {lbl} | {avg_wall:.1f} | {avg_tok:.1f} | {gpu_str} | "
                f"{gpu_delta_str} | {rl_str} | {pres_str} |"
            )

        lines.append("")

    report_path = output_dir / "kv_eval_results.md"
    output_dir.mkdir(parents=True, exist_ok=True)
    report_path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return report_path


