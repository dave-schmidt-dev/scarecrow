@dataclass
class RunResult:
    spec: RunSpec
    wall_s: float
    rss_peak_mb: float
    summary_path: Path | None
    footer: dict
    structure: dict[str, bool]
    error: str | None = None
    peak_memory_gb: float = 0.0

    @property
    def ok(self) -> bool:
        return self.error is None and self.summary_path is not None

    @property
    def tok_per_s(self) -> float:
        total_tokens = self.footer.get("total_tokens", 0)
        elapsed = self.footer.get("elapsed_s") or self.wall_s
        if elapsed > 0 and total_tokens > 0:
            return total_tokens / elapsed
        return 0.0

    @property
    def summary_words(self) -> int:
        return self.footer.get("summary_words", 0)


def _run_one(
    spec: RunSpec,
    transcript_path: Path,
    output_dir: Path,
) -> RunResult:
    """Execute a single benchmark run for *spec*."""
    print(f"\n  Running: {spec.label}")

    # Reset MLX peak memory before the run (Metal GPU only)
    try:
        import mlx.core as mx

        mx.reset_peak_memory()
        _mlx_available = True
    except ImportError:
        _mlx_available = False

    # Create a temp session dir with a copy of the transcript
    with tempfile.TemporaryDirectory(prefix="bench_summarizer_") as tmp_str:
        session_dir = Path(tmp_str)
        shutil.copy2(transcript_path, session_dir / "transcript.jsonl")

        # Override config values for this run.
        # Must set BOTH the Config instance AND the module-level alias —
        # _create_backend reads the module-level attribute, not the instance.
        orig_backend = _config_module.config.SUMMARIZER_BACKEND
        orig_kv_bits = _config_module.config.SUMMARIZER_MLX_KV_BITS
        orig_kv_bits_module = _config_module.SUMMARIZER_MLX_KV_BITS

        _config_module.config.SUMMARIZER_BACKEND = spec.backend
        if spec.kv_bits is not None:
            _config_module.config.SUMMARIZER_MLX_KV_BITS = spec.kv_bits
            _config_module.SUMMARIZER_MLX_KV_BITS = spec.kv_bits
        elif spec.backend == "mlx":
            # Ensure we use the non-kv-compressed default for plain MLX runs
            _config_module.config.SUMMARIZER_MLX_KV_BITS = None
            _config_module.SUMMARIZER_MLX_KV_BITS = None

        # Unique output name so we can copy it out afterward
        safe_label = spec.label.replace("/", "_")
        output_name = f"summary_{safe_label}.md"

        monitor = ResourceMonitor(interval=0.5)
        monitor.start()
        t0 = time.perf_counter()
        error: str | None = None
        summary_path_in_tmp: Path | None = None

        try:
            summary_path_in_tmp = summarize_session(
                session_dir,
                model=spec.model_key,
                output_name=output_name,
                backend=spec.backend,
            )
        except Exception as exc:
            error = str(exc)
        finally:
            wall_s = time.perf_counter() - t0
            snapshots = monitor.stop()
            # Restore config
            _config_module.config.SUMMARIZER_BACKEND = orig_backend
            _config_module.config.SUMMARIZER_MLX_KV_BITS = orig_kv_bits
            _config_module.SUMMARIZER_MLX_KV_BITS = orig_kv_bits_module

        rss_peak = peak_rss_mb(snapshots)

        # Read GPU peak memory after the run
        peak_memory_gb = 0.0
        if _mlx_available:
            try:
                import mlx.core as mx

                peak_memory_gb = mx.get_peak_memory() / 1e9
            except ImportError:
                pass

        # Parse summary and copy to output dir
        footer: dict = {}
        structure: dict[str, bool] = {}
        final_path: Path | None = None

        if summary_path_in_tmp is not None and summary_path_in_tmp.exists():
            summary_text = summary_path_in_tmp.read_text(encoding="utf-8")
            footer = parse_footer(summary_text)
            structure = check_structure(summary_text)

            # Copy to output dir for user inspection
            output_dir.mkdir(parents=True, exist_ok=True)
            dest = output_dir / output_name
            shutil.copy2(summary_path_in_tmp, dest)
            final_path = dest
            print(f"    Summary written: {dest}")
        elif error:
            print(f"    ERROR: {error}")
        else:
            print("    WARNING: summarize_session returned None")

        # Print quick stats
        elapsed_from_footer = footer.get("elapsed_s")
        elapsed_display = elapsed_from_footer if elapsed_from_footer else wall_s
        total_tokens = footer.get("total_tokens", 0)
        tok_s = (
            total_tokens / elapsed_display
            if elapsed_display > 0 and total_tokens
            else 0.0
        )
        words = footer.get("summary_words", 0)
        gpu_str = f"  |  GPU peak: {peak_memory_gb:.2f} GB" if peak_memory_gb else ""
        print(
            f"    Wall: {wall_s:.1f}s  |  LLM: {elapsed_display:.1f}s  |  "
            f"Tok/s: {tok_s:.1f}  |  Words: {words}  |  RSS peak: {rss_peak:.0f} MB"
            f"{gpu_str}"
        )
        if structure:
            checks = "  ".join(
                f"{'OK' if ok else 'MISSING'}: {sec.lstrip('#').strip()}"
                for sec, ok in structure.items()
            )
            print(f"    Structure: {checks}")

        return RunResult(
            spec=spec,
            wall_s=wall_s,
            rss_peak_mb=rss_peak,
            summary_path=final_path,
            footer=footer,
            structure=structure,
            error=error,
            peak_memory_gb=peak_memory_gb,
        )


