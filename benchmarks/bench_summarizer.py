def _detect_backends() -> list[str]:
    """Return list of available backend names."""
    available: list[str] = []

    # GGUF: check if any known model exists
    gguf_gemma4 = _discover_gguf(model_pattern=_MODEL_PATTERNS["gemma4"])
    if gguf_gemma4:
        available.append("gguf")

    # MLX: check if mlx-vlm is installed
    try:
        import mlx_vlm  # noqa: F401

        available.append("mlx")
    except ImportError:
        pass

    return available


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Benchmark summarizer backends (GGUF vs MLX) on a transcript"
    )
    parser.add_argument(
        "transcripts",
        type=Path,
        nargs="+",
        help="Path(s) to transcript.jsonl file(s) to summarize",
    )
    parser.add_argument(
        "--backends",
        type=str,
        default=None,
        help="Comma-separated list of backends to test (default: auto-detect). "
        "Options: gguf, mlx",
    )
    parser.add_argument(
        "--kv-bits",
        type=str,
        default=None,
        dest="kv_bits",
        help=(
            "Comma-separated TurboQuant KV cache bits for MLX backend "
            "(e.g. 4 or 4,8). Adds extra MLX runs with each compression level."
        ),
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=None,
        dest="output_dir",
        help=(
            "Directory for output summary files "
            "(default: first transcript's parent dir)"
        ),
    )
    args = parser.parse_args()

    # Validate transcript paths
    transcript_paths: list[Path] = []
    for raw in args.transcripts:
        p = raw.resolve()
        if not p.exists():
            print(f"ERROR: transcript not found: {p}")
            raise SystemExit(1)
        if not p.is_file():
            print(f"ERROR: not a file: {p}")
            raise SystemExit(1)
        transcript_paths.append(p)

    # Parse --kv-bits as comma-separated floats
    kv_bits_list: list[float] = []
    if args.kv_bits:
        for part in args.kv_bits.split(","):
            part = part.strip()
            if part:
                try:
                    kv_bits_list.append(float(part))
                except ValueError:
                    print(f"ERROR: invalid --kv-bits value: {part!r}")
                    raise SystemExit(1) from None

    base_output_dir: Path = (
        args.output_dir if args.output_dir else transcript_paths[0].parent
    )

    # Determine backends
    if args.backends:
        backends = [b.strip().lower() for b in args.backends.split(",") if b.strip()]
    else:
        print("Auto-detecting available backends...")
        backends = _detect_backends()
        if backends:
            print(f"  Found: {', '.join(backends)}")
        else:
            print("  No backends available. Install llama-cpp-python or mlx-vlm.")
            raise SystemExit(1)

    print(f"\nTranscripts: {', '.join(str(p) for p in transcript_paths)}")
    print(f"Backends:    {', '.join(backends)}")
    print(f"Output dir:  {base_output_dir}")
    if kv_bits_list:
        kv_display = ", ".join(str(int(k) if k == int(k) else k) for k in kv_bits_list)
        print(f"KV bits:     {kv_display} (MLX TurboQuant)")

    # Build run specs (shared across transcripts)
    print("\nBuilding run list...")
    specs = _build_run_specs(backends, kv_bits_list)

    if not specs:
        print("No runnable configurations found. Check backend availability.")
        raise SystemExit(1)

    print(f"Will run {len(specs)} configuration(s):")
    for spec in specs:
        kv_note = f" (kv_bits={spec.kv_bits:g})" if spec.kv_bits is not None else ""
        print(f"  {spec.label}{kv_note}")

    # Determine model name for report
    mlx_model_id = _config_module.config.SUMMARIZER_MLX_MODEL_ID
    report_model = mlx_model_id.split("/")[-1]

    # Execute runs — outer loop over transcripts
    all_results: dict[str, list[RunResult]] = {}

    for transcript_path in transcript_paths:
        # Use parent dir name as key (transcript files are all named transcript.jsonl)
        stem = transcript_path.parent.name or transcript_path.stem
        # Use per-transcript subdir when multiple transcripts provided
        if len(transcript_paths) > 1:
            output_dir = base_output_dir / stem
        else:
            output_dir = base_output_dir

        print(f"\n{'=' * 60}")
        print(f"  Transcript: {transcript_path.name}")
        print(f"{'=' * 60}")

        results: list[RunResult] = []
        for spec in specs:
            result = _run_one(spec, transcript_path, output_dir)
            results.append(result)

        all_results[stem] = results

        # Print per-transcript comparison table
        _print_table(results)

    # Write Markdown report
    report_path = _write_report(all_results, base_output_dir, report_model)
    print(f"Report written: {report_path}")

    # Exit with error if all runs failed across all transcripts
    all_run_results = [r for results in all_results.values() for r in results]
    if not any(r.ok for r in all_run_results):
        raise SystemExit(1)


if __name__ == "__main__":
    main()
