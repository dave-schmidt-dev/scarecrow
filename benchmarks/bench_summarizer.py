#!/usr/bin/env python3
"""Benchmark summarizer backends (GGUF vs MLX) on a real transcript.

Runs each (backend, model) combination against the same transcript.jsonl,
measures wall time and RSS memory, checks structural quality of the output,
and prints a comparison table.

Usage:
    uv run python benchmarks/bench_summarizer.py <transcript.jsonl>
    uv run python benchmarks/bench_summarizer.py <transcript.jsonl> --backends gguf,mlx
    uv run python benchmarks/bench_summarizer.py <transcript.jsonl> --kv-bits 4
    uv run python benchmarks/bench_summarizer.py <transcript.jsonl> \
        --output-dir /tmp/bench
"""

from __future__ import annotations

import argparse
import re
import shutil
import sys
import tempfile
import threading
import time
from dataclasses import dataclass, field
from pathlib import Path

import psutil

PROJECT_ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

from scarecrow import config as _config_module  # noqa: E402
from scarecrow.summarizer import (  # noqa: E402
    _MODEL_PATTERNS,
    _discover_gguf,
    summarize_session,
)

# ---------------------------------------------------------------------------
# Resource monitor (same pattern as bench_librispeech.py)
# ---------------------------------------------------------------------------


@dataclass
class ResourceSnapshot:
    timestamp: float
    rss_mb: float


@dataclass
class ResourceMonitor:
    """Background thread that samples RSS memory at intervals."""

    interval: float = 0.5
    snapshots: list[ResourceSnapshot] = field(default_factory=list)
    _stop: threading.Event = field(default_factory=threading.Event)
    _thread: threading.Thread | None = None
    _process: psutil.Process = field(default_factory=lambda: psutil.Process())

    def start(self) -> None:
        self.snapshots.clear()
        self._stop.clear()
        self._thread = threading.Thread(target=self._run, daemon=True)
        self._thread.start()

    def stop(self) -> list[ResourceSnapshot]:
        self._stop.set()
        if self._thread:
            self._thread.join(timeout=2)
        return self.snapshots

    def _run(self) -> None:
        while not self._stop.is_set():
            snap = ResourceSnapshot(
                timestamp=time.perf_counter(),
                rss_mb=self._process.memory_info().rss / (1024 * 1024),
            )
            self.snapshots.append(snap)
            self._stop.wait(self.interval)


def peak_rss_mb(snapshots: list[ResourceSnapshot]) -> float:
    if not snapshots:
        return 0.0
    return max(s.rss_mb for s in snapshots)


# ---------------------------------------------------------------------------
# Summary quality check
# ---------------------------------------------------------------------------

_REQUIRED_SECTIONS = ("## Summary", "## Key Points", "## Action Items")


def check_structure(summary_text: str) -> dict[str, bool]:
    """Return which required sections are present in the summary."""
    return {sec: sec in summary_text for sec in _REQUIRED_SECTIONS}


# ---------------------------------------------------------------------------
# Footer parsing
# ---------------------------------------------------------------------------

# Footer format (from summarizer.py):
# *Generated by Scarecrow · model: <name> · <N> words transcribed,
#  summarized in <M> words · <T> tokens used · <info> · <S>s*
_FOOTER_RE = re.compile(
    r"\*Generated by Scarecrow\s*·\s*"
    r"model:\s*(?P<model>[^·]+?)\s*·\s*"
    r"(?P<transcript_words>\d+)\s+words transcribed,\s*"
    r"summarized in\s*(?P<summary_words>\d+)\s+words\s*·\s*"
    r"(?P<total_tokens>\d+)\s+tokens used\s*·\s*"
    r"(?P<footer_info>[^·]+?)\s*·\s*"
    r"(?P<elapsed>[\d.]+)s\*"
)


def parse_footer(summary_text: str) -> dict:
    """Extract metrics from the *Generated by Scarecrow* footer line."""
    m = _FOOTER_RE.search(summary_text)
    if not m:
        return {}
    return {
        "model": m.group("model").strip(),
        "transcript_words": int(m.group("transcript_words")),
        "summary_words": int(m.group("summary_words")),
        "total_tokens": int(m.group("total_tokens")),
        "elapsed_s": float(m.group("elapsed")),
        "footer_info": m.group("footer_info").strip(),
    }


# ---------------------------------------------------------------------------
# Run spec definitions
# ---------------------------------------------------------------------------


@dataclass
class RunSpec:
    """Describes a single benchmark run."""

    label: str
    backend: str
    model_key: str | None  # key into _MODEL_PATTERNS (GGUF) or None
    kv_bits: int | None = None  # MLX TurboQuant only


def _build_run_specs(
    backends: list[str],
    kv_bits: int | None,
) -> list[RunSpec]:
    """Build the list of RunSpec entries, skipping unavailable combinations."""
    specs: list[RunSpec] = []

    for backend in backends:
        if backend == "gguf":
            # Default GGUF — gemma-3-27b pattern
            gguf_gemma3 = _discover_gguf(model_pattern=_MODEL_PATTERNS["gemma"])
            if gguf_gemma3 is not None:
                specs.append(
                    RunSpec(
                        label="gguf/gemma-3",
                        backend="gguf",
                        model_key="gemma",
                    )
                )
            else:
                print(
                    "  [skip] gguf/gemma-3: no GGUF found for pattern "
                    f"{_MODEL_PATTERNS['gemma']!r}"
                )

            # gemma4 GGUF — only if available
            gguf_gemma4 = _discover_gguf(model_pattern=_MODEL_PATTERNS["gemma4"])
            if gguf_gemma4 is not None:
                specs.append(
                    RunSpec(
                        label="gguf/gemma-4",
                        backend="gguf",
                        model_key="gemma4",
                    )
                )
            else:
                print(
                    "  [skip] gguf/gemma-4: no GGUF found for pattern "
                    f"{_MODEL_PATTERNS['gemma4']!r}"
                )

        elif backend == "mlx":
            # Check that mlx-vlm is installed
            try:
                import mlx_vlm  # noqa: F401

                mlx_available = True
            except ImportError:
                mlx_available = False

            if not mlx_available:
                print(
                    "  [skip] mlx: mlx-vlm not installed. "
                    "Run: uv sync --extra mlx-summarizer"
                )
                continue

            # Default MLX model
            mlx_model_id = _config_module.config.SUMMARIZER_MLX_MODEL_ID
            specs.append(
                RunSpec(
                    label=f"mlx/{mlx_model_id.split('/')[-1]}",
                    backend="mlx",
                    model_key=None,
                )
            )

            # MLX + TurboQuant KV cache (only if --kv-bits specified)
            if kv_bits is not None:
                specs.append(
                    RunSpec(
                        label=f"mlx/{mlx_model_id.split('/')[-1]}/kv{kv_bits}",
                        backend="mlx",
                        model_key=None,
                        kv_bits=kv_bits,
                    )
                )

        else:
            print(f"  [skip] unknown backend: {backend!r}")

    return specs


# ---------------------------------------------------------------------------
# Per-run execution
# ---------------------------------------------------------------------------


@dataclass
class RunResult:
    spec: RunSpec
    wall_s: float
    rss_peak_mb: float
    summary_path: Path | None
    footer: dict
    structure: dict[str, bool]
    error: str | None = None

    @property
    def ok(self) -> bool:
        return self.error is None and self.summary_path is not None

    @property
    def tok_per_s(self) -> float:
        total_tokens = self.footer.get("total_tokens", 0)
        elapsed = self.footer.get("elapsed_s") or self.wall_s
        if elapsed > 0 and total_tokens > 0:
            return total_tokens / elapsed
        return 0.0

    @property
    def summary_words(self) -> int:
        return self.footer.get("summary_words", 0)


def _run_one(
    spec: RunSpec,
    transcript_path: Path,
    output_dir: Path,
) -> RunResult:
    """Execute a single benchmark run for *spec*."""
    print(f"\n  Running: {spec.label}")

    # Create a temp session dir with a copy of the transcript
    with tempfile.TemporaryDirectory(prefix="bench_summarizer_") as tmp_str:
        session_dir = Path(tmp_str)
        shutil.copy2(transcript_path, session_dir / "transcript.jsonl")

        # Override config values for this run
        orig_backend = _config_module.config.SUMMARIZER_BACKEND
        orig_kv_bits = _config_module.config.SUMMARIZER_MLX_KV_BITS

        _config_module.config.SUMMARIZER_BACKEND = spec.backend
        if spec.kv_bits is not None:
            _config_module.config.SUMMARIZER_MLX_KV_BITS = spec.kv_bits
        elif spec.backend == "mlx":
            # Ensure we use the non-kv-compressed default for plain MLX runs
            _config_module.config.SUMMARIZER_MLX_KV_BITS = None

        # Unique output name so we can copy it out afterward
        safe_label = spec.label.replace("/", "_")
        output_name = f"summary_{safe_label}.md"

        monitor = ResourceMonitor(interval=0.5)
        monitor.start()
        t0 = time.perf_counter()
        error: str | None = None
        summary_path_in_tmp: Path | None = None

        try:
            summary_path_in_tmp = summarize_session(
                session_dir,
                model=spec.model_key,
                output_name=output_name,
                backend=spec.backend,
            )
        except Exception as exc:
            error = str(exc)
        finally:
            wall_s = time.perf_counter() - t0
            snapshots = monitor.stop()
            # Restore config
            _config_module.config.SUMMARIZER_BACKEND = orig_backend
            _config_module.config.SUMMARIZER_MLX_KV_BITS = orig_kv_bits

        rss_peak = peak_rss_mb(snapshots)

        # Parse summary and copy to output dir
        footer: dict = {}
        structure: dict[str, bool] = {}
        final_path: Path | None = None

        if summary_path_in_tmp is not None and summary_path_in_tmp.exists():
            summary_text = summary_path_in_tmp.read_text(encoding="utf-8")
            footer = parse_footer(summary_text)
            structure = check_structure(summary_text)

            # Copy to output dir for user inspection
            output_dir.mkdir(parents=True, exist_ok=True)
            dest = output_dir / output_name
            shutil.copy2(summary_path_in_tmp, dest)
            final_path = dest
            print(f"    Summary written: {dest}")
        elif error:
            print(f"    ERROR: {error}")
        else:
            print("    WARNING: summarize_session returned None")

        # Print quick stats
        elapsed_from_footer = footer.get("elapsed_s")
        elapsed_display = elapsed_from_footer if elapsed_from_footer else wall_s
        total_tokens = footer.get("total_tokens", 0)
        tok_s = (
            total_tokens / elapsed_display
            if elapsed_display > 0 and total_tokens
            else 0.0
        )
        words = footer.get("summary_words", 0)
        print(
            f"    Wall: {wall_s:.1f}s  |  LLM: {elapsed_display:.1f}s  |  "
            f"Tok/s: {tok_s:.1f}  |  Words: {words}  |  RSS peak: {rss_peak:.0f} MB"
        )
        if structure:
            checks = "  ".join(
                f"{'OK' if ok else 'MISSING'}: {sec.lstrip('#').strip()}"
                for sec, ok in structure.items()
            )
            print(f"    Structure: {checks}")

        return RunResult(
            spec=spec,
            wall_s=wall_s,
            rss_peak_mb=rss_peak,
            summary_path=final_path,
            footer=footer,
            structure=structure,
            error=error,
        )


# ---------------------------------------------------------------------------
# Comparison table
# ---------------------------------------------------------------------------


def _print_table(results: list[RunResult]) -> None:
    """Print a formatted comparison table of all results."""
    print(f"\n{'=' * 70}")
    print("  SUMMARIZER BACKEND COMPARISON")
    print(f"{'=' * 70}")
    col = 38
    print(
        f"  {'Label':<{col}} {'Wall(s)':>7}  {'Tok/s':>5}  {'Words':>5}  {'RSS MB':>6}"
    )
    print(f"  {'-' * col} {'-------':>7}  {'-----':>5}  {'-----':>5}  {'------':>6}")
    for r in results:
        if r.error:
            err = r.error[:40]
            print(
                f"  {r.spec.label:<{col}} "
                f"{'ERROR':>7}  {'':>5}  {'':>5}  "
                f"{'':>6}  ({err})"
            )
            continue
        wall_s = r.wall_s
        tok_s = r.tok_per_s
        words = r.summary_words
        rss = r.rss_peak_mb
        print(
            f"  {r.spec.label:<{col}} "
            f"{wall_s:>7.1f}  {tok_s:>5.1f}  "
            f"{words:>5}  {rss:>6.0f}"
        )
    print(f"{'=' * 70}")

    # Structure quality summary
    if any(r.structure for r in results if r.ok):
        print("\n  Structure quality:")
        for r in results:
            if not r.ok or not r.structure:
                continue
            checks = "  ".join(
                ("+" if ok else "-") + " " + sec.lstrip("#").strip()
                for sec, ok in r.structure.items()
            )
            print(f"  {r.spec.label:<{col}} {checks}")
    print()


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------


def _detect_backends() -> list[str]:
    """Return list of available backend names."""
    available: list[str] = []

    # GGUF: check if any known model exists
    gguf_gemma3 = _discover_gguf(model_pattern=_MODEL_PATTERNS["gemma"])
    gguf_gemma4 = _discover_gguf(model_pattern=_MODEL_PATTERNS["gemma4"])
    if gguf_gemma3 or gguf_gemma4:
        available.append("gguf")

    # MLX: check if mlx-vlm is installed
    try:
        import mlx_vlm  # noqa: F401

        available.append("mlx")
    except ImportError:
        pass

    return available


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Benchmark summarizer backends (GGUF vs MLX) on a transcript"
    )
    parser.add_argument(
        "transcript",
        type=Path,
        help="Path to transcript.jsonl to summarize",
    )
    parser.add_argument(
        "--backends",
        type=str,
        default=None,
        help="Comma-separated list of backends to test (default: auto-detect). "
        "Options: gguf, mlx",
    )
    parser.add_argument(
        "--kv-bits",
        type=int,
        default=None,
        dest="kv_bits",
        help="TurboQuant KV cache bits for MLX backend (e.g. 4). "
        "Adds an extra MLX run with compression enabled.",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=None,
        dest="output_dir",
        help="Directory for output summary files (default: transcript's parent dir)",
    )
    args = parser.parse_args()

    transcript_path: Path = args.transcript.resolve()
    if not transcript_path.exists():
        print(f"ERROR: transcript not found: {transcript_path}")
        raise SystemExit(1)
    if not transcript_path.is_file():
        print(f"ERROR: not a file: {transcript_path}")
        raise SystemExit(1)

    output_dir: Path = args.output_dir if args.output_dir else transcript_path.parent

    # Determine backends
    if args.backends:
        backends = [b.strip().lower() for b in args.backends.split(",") if b.strip()]
    else:
        print("Auto-detecting available backends...")
        backends = _detect_backends()
        if backends:
            print(f"  Found: {', '.join(backends)}")
        else:
            print("  No backends available. Install llama-cpp-python or mlx-vlm.")
            raise SystemExit(1)

    print(f"\nTranscript: {transcript_path}")
    print(f"Backends:   {', '.join(backends)}")
    print(f"Output dir: {output_dir}")
    if args.kv_bits:
        print(f"KV bits:    {args.kv_bits} (MLX TurboQuant)")

    # Build run specs
    print("\nBuilding run list...")
    specs = _build_run_specs(backends, args.kv_bits)

    if not specs:
        print("No runnable configurations found. Check backend availability.")
        raise SystemExit(1)

    print(f"Will run {len(specs)} configuration(s):")
    for spec in specs:
        kv_note = f" (kv_bits={spec.kv_bits})" if spec.kv_bits else ""
        print(f"  {spec.label}{kv_note}")

    # Execute runs
    results: list[RunResult] = []
    for spec in specs:
        result = _run_one(spec, transcript_path, output_dir)
        results.append(result)

    # Print comparison table
    _print_table(results)

    # Exit with error if all runs failed
    if not any(r.ok for r in results):
        raise SystemExit(1)


if __name__ == "__main__":
    main()
