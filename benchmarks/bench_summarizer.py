# Comparison table
# ---------------------------------------------------------------------------


def _print_table(results: list[RunResult]) -> None:
    """Print a formatted comparison table of all results."""
    print(f"\n{'=' * 80}")
    print("  SUMMARIZER BACKEND COMPARISON")
    print(f"{'=' * 80}")
    col = 38
    print(
        f"  {'Label':<{col}} {'Wall(s)':>7}  {'Tok/s':>5}  {'Words':>5}  "
        f"{'RSS MB':>6}  {'GPU GB':>6}"
    )
    print(
        f"  {'-' * col} {'-------':>7}  {'-----':>5}  {'-----':>5}  "
        f"{'------':>6}  {'------':>6}"
    )
    for r in results:
        if r.error:
            err = r.error[:40]
            print(
                f"  {r.spec.label:<{col}} "
                f"{'ERROR':>7}  {'':>5}  {'':>5}  "
                f"{'':>6}  {'':>6}  ({err})"
            )
            continue
        wall_s = r.wall_s
        tok_s = r.tok_per_s
        words = r.summary_words
        rss = r.rss_peak_mb
        gpu = r.peak_memory_gb
        gpu_str = f"{gpu:>6.2f}" if gpu else f"{'—':>6}"
        print(
            f"  {r.spec.label:<{col}} "
            f"{wall_s:>7.1f}  {tok_s:>5.1f}  "
            f"{words:>5}  {rss:>6.0f}  {gpu_str}"
        )
    print(f"{'=' * 80}")

    # Structure quality summary
    if any(r.structure for r in results if r.ok):
        print("\n  Structure quality:")
        for r in results:
            if not r.ok or not r.structure:
                continue
            checks = "  ".join(
                ("+" if ok else "-") + " " + sec.lstrip("#").strip()
                for sec, ok in r.structure.items()
            )
            print(f"  {r.spec.label:<{col}} {checks}")
    print()


