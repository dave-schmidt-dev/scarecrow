#!/usr/bin/env python3
"""Benchmark summarizer backends (GGUF vs MLX) on a real transcript.

Runs each (backend, model) combination against the same transcript.jsonl,
measures wall time and RSS memory, checks structural quality of the output,
and prints a comparison table.

Usage:
    uv run python benchmarks/bench_summarizer.py <transcript.jsonl>
    uv run python benchmarks/bench_summarizer.py <transcript.jsonl> --backends gguf,mlx
    uv run python benchmarks/bench_summarizer.py <transcript.jsonl> --kv-bits 4
    uv run python benchmarks/bench_summarizer.py <transcript.jsonl> --kv-bits 4,8
    uv run python benchmarks/bench_summarizer.py t1.jsonl t2.jsonl --kv-bits 4,8
    uv run python benchmarks/bench_summarizer.py <transcript.jsonl> \
        --output-dir /tmp/bench
"""

