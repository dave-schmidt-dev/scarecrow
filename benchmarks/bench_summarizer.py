_REQUIRED_SECTIONS = ("## Summary", "## Key Points", "## Action Items")


def check_structure(summary_text: str) -> dict[str, bool]:
    """Return which required sections are present in the summary."""
    return {sec: sec in summary_text for sec in _REQUIRED_SECTIONS}


# ---------------------------------------------------------------------------
# Footer parsing
# ---------------------------------------------------------------------------

# Footer format (from summarizer.py):
# *Generated by Scarecrow · model: <name> · <N> words transcribed,
#  summarized in <M> words · <T> tokens used · <info> · <S>s*
# New formats also include trailing fields after elapsed:
#   ...{elapsed}s · session: {duration}*
#   ...{elapsed}s · {N} segments synthesized*
_FOOTER_RE = re.compile(
    r"\*Generated by Scarecrow\s*·\s*"
    r"model:\s*(?P<model>[^·]+?)\s*·\s*"
    r"(?P<transcript_words>\d+)\s+words transcribed,\s*"
    r"summarized in\s*(?P<summary_words>\d+)\s+words\s*·\s*"
    r"(?P<total_tokens>\d+)\s+tokens used\s*·\s*"
    r"(?P<footer_info>.+)\s*·\s*"
    r"(?P<elapsed>[\d.]+)s"
    r"(?:\s*·\s*[^*]*)?"  # optional trailing fields
    r"\*"
)


def parse_footer(summary_text: str) -> dict:
    """Extract metrics from the *Generated by Scarecrow* footer line."""
    m = _FOOTER_RE.search(summary_text)
    if not m:
        return {}
    return {
        "model": m.group("model").strip(),
        "transcript_words": int(m.group("transcript_words")),
        "summary_words": int(m.group("summary_words")),
        "total_tokens": int(m.group("total_tokens")),
        "elapsed_s": float(m.group("elapsed")),
        "footer_info": m.group("footer_info").strip(),
    }


# ---------------------------------------------------------------------------
# Quality comparison helpers
# ---------------------------------------------------------------------------


def _lcs_length(a: list[str], b: list[str]) -> int:
    """Length of the longest common subsequence."""
    m, n = len(a), len(b)
    if m == 0 or n == 0:
        return 0
    prev = [0] * (n + 1)
    curr = [0] * (n + 1)
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            if a[i - 1] == b[j - 1]:
                curr[j] = prev[j - 1] + 1
            else:
                curr[j] = max(prev[j], curr[j - 1])
        prev, curr = curr, [0] * (n + 1)
    return prev[n]


def rouge_l(reference: str, hypothesis: str) -> float:
    """Compute ROUGE-L F1 between reference and hypothesis."""
    ref_tokens = reference.lower().split()
    hyp_tokens = hypothesis.lower().split()
    if not ref_tokens or not hyp_tokens:
        return 0.0
    lcs = _lcs_length(ref_tokens, hyp_tokens)
    precision = lcs / len(hyp_tokens)
    recall = lcs / len(ref_tokens)
    if precision + recall == 0:
        return 0.0
    return 2 * precision * recall / (precision + recall)


_ACTION_ITEM_RE = re.compile(r"^- \[ \]\s+(.+)", re.MULTILINE)


def _extract_action_items(text: str) -> list[str]:
    """Extract action item text from summary markdown."""
    return [m.group(1).strip().lower() for m in _ACTION_ITEM_RE.finditer(text)]


def _compare_quality(baseline_text: str, variant_text: str) -> dict:
    """Compare a variant summary against the baseline."""
    rl = rouge_l(baseline_text, variant_text)
    baseline_items = _extract_action_items(baseline_text)
    variant_items = _extract_action_items(variant_text)
    if baseline_items:
        preserved = sum(
            1
            for item in baseline_items
            if any(item in vi or vi in item for vi in variant_items)
        )
        preservation_rate = preserved / len(baseline_items)
    else:
        preserved = 0
        preservation_rate = 1.0
    return {
        "rouge_l": rl,
        "action_items_baseline": len(baseline_items),
        "action_items_preserved": preserved,
        "preservation_rate": preservation_rate,
    }


