# ---------------------------------------------------------------------------
# Resource monitor (same pattern as bench_librispeech.py)
# ---------------------------------------------------------------------------


@dataclass
class ResourceSnapshot:
    timestamp: float
    rss_mb: float


@dataclass
class ResourceMonitor:
    """Background thread that samples RSS memory at intervals."""

    interval: float = 0.5
    snapshots: list[ResourceSnapshot] = field(default_factory=list)
    _stop: threading.Event = field(default_factory=threading.Event)
    _thread: threading.Thread | None = None
    _process: psutil.Process = field(default_factory=lambda: psutil.Process())

    def start(self) -> None:
        self.snapshots.clear()
        self._stop.clear()
        self._thread = threading.Thread(target=self._run, daemon=True)
        self._thread.start()

    def stop(self) -> list[ResourceSnapshot]:
        self._stop.set()
        if self._thread:
            self._thread.join(timeout=2)
        return self.snapshots

    def _run(self) -> None:
        while not self._stop.is_set():
            snap = ResourceSnapshot(
                timestamp=time.perf_counter(),
                rss_mb=self._process.memory_info().rss / (1024 * 1024),
            )
            self.snapshots.append(snap)
            self._stop.wait(self.interval)


def peak_rss_mb(snapshots: list[ResourceSnapshot]) -> float:
    if not snapshots:
        return 0.0
    return max(s.rss_mb for s in snapshots)


